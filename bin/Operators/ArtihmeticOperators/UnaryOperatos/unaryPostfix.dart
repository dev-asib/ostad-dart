void main(){

  //unary postfix (Incremeent)
  int number1=10;
  print(number1++);
  print(number1);

  number1++;
  print(number1);

  number1++;
  number1;
  print(number1);

  //unary postfix (Dncremeent)
  int number2=20;
  print(number2--);
  print(number2);

  number2--;
  print(number2);

  number2--;
  number2;
  print(number2);
}